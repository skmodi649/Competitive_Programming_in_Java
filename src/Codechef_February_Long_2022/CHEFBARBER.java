package Codechef_February_Long_2022;

import java.util.Scanner;

public class CHEFBARBER {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        for(int a = 1 ; a <= t ; a++){
            int N = sc.nextInt();
            int M = sc.nextInt();
            System.out.println(N*M);
        }
    }
}
